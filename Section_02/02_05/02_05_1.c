// 2.5 변수를 선언하는 방법

int main(void)
{
	// 변수의 선언 (declaration)
	// 정수 하나를 저장할 공간을 확보했으며 (allocation)
	// 해당 변수를 통해 해당 공간에 접근할 수 있다.
	int x;
	int y;
	int z;

	// 변수에 값을 대입 (assignment) 또는 초기화(initialization)
	// 변수 x에 1을 대입
	// 변수 y에 2을 대입
	x = 1;
	y = 2;
	
	// 변수에 연산(operation)의 결과를 대입
	z = x + y;

	return 0;
}